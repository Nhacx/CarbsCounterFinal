<h1> O que é Diabetes Mellitus? </h1>
</br>

<p id="p1"> Diabetes Mellitus é uma doença caracterizada pela elevação da glicose no sangue (hiperglicemia). Pode ocorrer devido a defeitos na secreção ou na ação do hormônio insulina, que é produzido no pâncreas, pelas chamadas células beta . A função principal da insulina é promover a entrada de glicose para as células do organismo de forma que ela possa ser aproveitada para as diversas atividades celulares. A falta da insulina ou um defeito na sua ação resulta portanto em acúmulo de glicose no sangue, o que chamamos de hiperglicemia.</p>
</br>
<p id="p1">Classificação do Diabetes</p>
</br>
<p id="p1">Sabemos hoje que diversas condições que podem levar ao diabetes, porém a grande maioria dos casos está dividida em dois grupos: Diabetes Tipo 1 e Diabetes Tipo 2.</p>
</br>
<p id="p1">Diabetes Tipo 1 (DM 1) – Essa forma de diabetes é resultado da destruição das células beta pancreáticas por um processo imunológico, ou seja, pela formação de anticorpos pelo próprio organismo contra as células, beta levando a deficiência de insulina. Nesse caso podemos detectar em exames de sangue a presença desses anticorpos que são: ICA, IAAs, GAD e IA-2. Eles estão presentes em cerca de 85 a 90% dos casos de DM 1 no momento do diagnóstico. Em geral costuma acometer crianças e adultos jovens, mas pode ser desencadeado em qualquer faixa etária.</p>
</br>
<p id="p1">O quadro clínico mais característico é de um início relativamente rápido (alguns dias até poucos meses) de sintomas como: sede, diurese e fome excessivas, emagrecimento importante, cansaço e fraqueza. Se o tratamento não for realizado rapidamente, os sintomas podem evoluir para desidratação severa, sonolência, vômitos, dificuldades respiratórias e coma. Esse quadro mais grave é conhecido como Cetoacidose Diabética e necessita de internação para tratamento.</p>
</br>
<p id="p1">Diabetes Tipo 2 (DM 2) – Nesta forma de diabetes está incluída a grande maioria dos casos (cerca de 90% dos pacientes diabéticos). Nesses pacientes, a insulina é produzida pelas células beta pancreáticas, porém, sua ação está dificultada, caracterizando um quadro de resistência insulínica. Isso vai levar a um aumento da produção de insulina para tentar manter a glicose em níveis normais. Quando isso não é mais possível, surge o diabetes. A instalação do quadro é mais lenta e os sintomas – sede, aumento da diurese, dores nas pernas, alterações visuais e outros – podem demorar vários anos até se apresentarem. Se não reconhecido e tratado a tempo, também pode evoluir para um quadro grave de desidratação e coma .</p>
</br>
<p id="p1">Ao contrário do Diabetes Tipo 1, há geralmente associação com aumento de peso e obesidade, acometendo principalmente adultos a partir dos 50 anos. Contudo, observa-se, cada vez mais, o desenvolvimento do quadro em adultos jovens e até crianças. Isso se deve, principalmente, pelo aumento do consumo de gorduras e carboidratos aliados à falta de atividade física. Assim, o endocrinologista tem, mais do que qualquer outro especialista, a chance de diagnosticar o diabetes em sua fase inicial, haja visto a grande quantidade de pacientes que procuram este profissional por problemas de obesidade.</p>
</br>
<p id="p1">Outros Tipos de Diabetes – Outros tipos de diabetes são bem mais raros e incluem defeitos genéticos da função da célula beta (MODY 1, 2 e 3), defeitos genéticos na ação da insulina, doenças do pâncreas (pancreatite, tumores pancreáticos, hemocromatose), outras doenças endócrinas (Síndrome de Cushing, hipertireoidismo, acromegalia) e uso de certos medicamentos.</p>
</br>
<p id="p1">Diabetes Gestacional – Atenção especial deve ser dada ao diabetes diagnosticado durante a gestação. A ele é dado o nome de Diabetes Gestacional. Pode ser transitório ou não e, ao término da gravidez, a paciente deve ser investigada e acompanhada.. Na maioria das vezes ele é detectado no 3o trimestre da gravidez, através de um teste de sobrecarga de glicose. As gestantes que tiverem história prévia de diabetes gestacional, de perdas fetais, má formações fetais, hipertensão arterial, obesidade ou história familiar de diabetes não devem esperar o 3º trimestre para serem testadas, já que sua chance de desenvolverem a doença é maior.</p>
</br>
<p id="p1">Como Posso Saber se Estou Diabético?</p>
</br>
<p id="p1">O diagnóstico laboratorial pode ser feito de três formas e, caso positivo, deve ser confirmado em outra ocasião. São considerados positivos os que apresentarem os seguintes resultados:</p>
</br>
1) glicemia de jejum > 126 mg/dl (jejum de 8 horas)
</br>
2) glicemia casual (colhida em qualquer horário do dia, independente da última refeição realizada (> 200 mg/dl em paciente com sintomas característicos de diabetes.
</br>
3) glicemia > 200 mg/dl duas horas após sobrecarga oral de 75 gramas de glicose.
</br>
<p id="p1">Existem ainda dois grupos de pacientes, identificados por esses mesmos exames, que devem ser acompanhados de perto pois tem grande chance de tornarem-se diabéticos. Na verdade esses pacientes já devem ser submetidos a um tratamento preventivo que inclui mudança de hábitos alimentares, prática de atividade física ou mesmo a introdução de medicamentos. São eles:</p>
</br>
(a) glicemia de jejum > 110mg/dl e < 126 mg/dl.
</br>
(b) glicemia 2 horas após sobrecarga de 75 gr de glicose oral entre 140 mg/dl e 200 mg/dl
</br>
<p id="p1">O diagnóstico precoce do diabetes é importante não só para prevenção das complicações agudas já descritas, como também para a prevenção de complicações crônicas.</p>
</br>
<p id="p1">A Importância do Acompanhamento Médico</p>
</br>
<p id="p1">É importante que o paciente compareça às consultas regularmente, conforme a determinação médica, nas quais ele deverá receber orientações sobre a doença e seu tratamento. Só um especialista saberá indicar de forma correta:</p>
</br>
• a orientação nutricional adequada,
</br>
• como evitar complicações,
</br>
• como usar insulina ou outros medicamentos,
</br>
• como usar os aparelhos que medem a glicose (glicosímetros) e as canetas de insulina,
</br>
• fornecer orientações sobre atividade física,
</br>
• fornecer orientações de como proceder em situações de hipo e de hiperglicemia.
</br>
<p id="p1">Esse aprendizado é fundamental não só para o bom controle do diabetes como também para garantir autonomia e independência ao paciente. É muito importante que ele realize suas atividades de rotina, viajar ou praticar esportes com muito mais segurança. É importante o envolvimento dos familiares com o tratamento do paciente diabético, visto que, muitas vezes, há uma mudança de hábitos, requerendo a adaptação de todo núcleo familiar.</p>
</br>
<p id="p1">Por que Tratar a Hiperglicemia?</p>
</br>
<p id="p1">A hiperglicemia é a elevação das taxas de açúcar no sangue e que deve ser controlada. Sabe-se que a hiperglicemia crônica através dos anos está associada a lesões da microcirculação, lesando e prejudicando o funcionamento de vários órgãos como os rins, os olhos, os nervos e o coração. Os pacientes que conseguem manter um bom controle da glicemia têm uma importante redução no risco de desenvolver tais complicações como já ficou demonstrado em vários estudos científicos.</p>
</br>
<p id="p1">Pacientes com Diabetes Tipo 2 não diagnosticado tem risco maior de apresentar acidente vascular cerebral, infarto do miocárdio e doença vascular periférica do que pessoas que não têm diabetes. Isso reforça a necessidade de um diagnóstico precoce que permita evitar tais complicações.</p>
</br>
<p id="p1">A Automonitorização</p>
</br>
<p id="p1">Para obter um melhor controle dos níveis glicêmicos, não basta o paciente apenas acreditar que está fazendo tudo corretamente ou ter a sensação de estar sentindo-se “bem”. É necessário monitorar, no dia-a-dia, os níveis glicêmicos. Para isso, existem modernos aparelhos, os glicosímetros, de fácil utilização e que nos fornecem o resultado da glicemia em alguns segundos. Siga as orientações do seu médico quanto ao número de testes que deve ser realizado.</p>
</br>
<p id="p1">O objetivo desse controle não é só corrigir as eventuais hiperglicemias que ocorrerão, mas também tentar manter a glicemia o mais próximo da normalidade, sem causar hipoglicemia.</p>
</br>
<p id="p1">Quanto melhor o controle, maior o risco de hipoglicemia, daí a importância também da monitorização da glicemia mais vezes tanto para evitar a hipo, como também para que não se coma em excesso na correção dela, o que invalidaria os esforços para manter o controle. A monitorização permite que o paciente, individualmente, avalie sua resposta aos alimentos, aos medicamentos (especialmente à insulina) e à atividade física praticada.</p>
</br>
<p id="p1">Exames de Rotina</p>
</br>
<p id="p1">De acordo com a necessidade, as consultas devem ser mensais, bimestrais ou trimestrais, com eventuais contatos por telefone ou fax, com envio da monitorização glicêmica. Nas consultas são solicitados os exames que devem incluir a glicemia, a hemoglobina glicada trimestral (que dá a média da glicemia diária nos últimos 2 a 3 meses), função renal anual (uréia, creatinina, pesquisa de micralbuminúria), perfil lipídico anual ou semestral, avaliação oftalmológica anual, avaliação cardiológica. Os demais exames devem ser solicitados de acordo com a necessidade individual do paciente.
</p>

